def mobilenetv2(pretrained='imagenet', type='mobilenetv2_10'):
    """
    Constructs a MobileNet V2 model
    """

    if pretrained:
        settings = pretrained_settings[type][pretrained]
        model = MobileNetV2(num_classes=settings['num_classes'], width_mult=settings['width_mult'], input_size=settings['input_size'])
        model.load_state_dict(model_zoo.load_url(settings['url']), strict=False)

        model.input_space = settings['input_space']
        model.input_size = settings['input_size']
        model.input_range = settings['input_range']
        model.mean = settings['mean']
        model.std = settings['std']

    else:
        model = MobileNetV2()

    return model
